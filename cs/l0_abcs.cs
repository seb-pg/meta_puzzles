class Solution {
  
    public int getSum(int A, int B, int C) {
        // https://www.metacareers.com/profile/coding_puzzles/?puzzle=513411323351554
        // Constraints
        //   1 ≤ A,B,C ≤ 100
        // Complexity: O(1)

        return A + B + C;
    }
  
}

